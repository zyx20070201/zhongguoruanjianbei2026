export const reactChatVisualUserPrompt = (
  userPrompt: string,
  sources: Array<{ id: string; name: string; path?: string; content: string }>
) => [
  '请直接回答用户问题；如果适合可视化，就在回答中直接嵌入 `~~~REACT_VIZ` 或 `~~~HTML_VIZ` 代码块。',
  '不要输出 JSON，不要输出 schema，不要解释系统规则。',
  '标记外如需展示多行代码、伪代码、SQL、命令或配置，必须用标准 Markdown fenced code block 包起来，禁止输出裸缩进代码。',
  '如果主题涉及算法、数据结构、流程、状态变化、表结构或执行过程，优先做成可逐步观察的交互演示，而不是只展示初始态和最终态。',
  '如果是算法或数据结构，尽量显式展示中间步骤、当前状态、关键比较、交换/旋转/入队/出队等过程。',
  '如果生成 React 可视化组件，必须按 Header / Visualization / Controls / StateSummary + CurrentExplanation / Explanation 的顺序排版。',
  'Controls 必须靠近主可视化区域，尤其是树、图、数组、表格等主视图；不要只把回到开始、上一步、下一步、跳到结束放在组件顶部。',
  'StateSummary 和 CurrentExplanation 都必须跟随当前步骤变化，回答“当前画面是什么状态”和“为什么这一步这样做”。',
  '如果有多个视图，把它们归入 Visualization 区域：主视图优先，辅助视图次之；不要让辅助视图把主操作区挤到很远的位置。',
  '',
  `用户要求：${userPrompt || '生成一个可视化讲解。'}`,
  '',
  '# 用户勾选资料全文',
  sources.length
    ? sources.map((source, index) => [
      `## Source ${index + 1}: ${source.name}`,
      `SourceId: ${source.id}`,
      source.path ? `Path: ${source.path}` : '',
      '',
      source.content || '(empty source text)'
    ].filter(Boolean).join('\n')).join('\n\n---\n\n')
    : '(no selected source text)'
].join('\n');
